#  NoneType, with a single value, None
winner = None 
print('winner:', winner) 
print('winner is None:', winner is None) 
print('winner is not None:', winner is not None) 
print(type(winner)) 
print('Set winner to True') 
winner = True 
print('winner:', winner) 
print('winner is None:', winner is None) 
print('winner is not None:', winner is not None) 
print(type(winner))
